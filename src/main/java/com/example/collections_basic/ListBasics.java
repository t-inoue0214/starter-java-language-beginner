package com.example.collections_basic;

import java.util.ArrayList;
import java.util.List;

/**
 * List（リスト）の基本操作を学ぶクラスです。
 * 配列と違って、サイズを気にせず `add` や `remove` ができます。
 */
public class ListBasics {

    public static void main(String[] args) {
        
        // ---------------------------------------------------------
        // 1. Listの宣言と作成
        // List<中身の型> 変数名 = new ArrayList<>();
        // 「文字列(String)専用のリスト」を作ります。
        // ---------------------------------------------------------
        System.out.println("=== List (ArrayList) の操作 ===");
        
        List<String> party = new ArrayList<>();

        // ---------------------------------------------------------
        // 2. データの追加 (add)
        // ---------------------------------------------------------
        party.add("勇者");
        party.add("戦士");
        party.add("魔法使い");
        
        // toString()が最初から綺麗に実装されているので、中身が見やすい
        System.out.println("現在のパーティ: " + party); 
        // -> [勇者, 戦士, 魔法使い]

        // ---------------------------------------------------------
        // 3. データの取得 (get) と サイズ確認 (size)
        // 配列の box[0] は、リストでは box.get(0) になります。
        // ---------------------------------------------------------
        System.out.println("リーダーは: " + party.get(0));
        System.out.println("現在の人数: " + party.size() + "人");

        // ---------------------------------------------------------
        // 4. データの削除 (remove)
        // 戦士(インデックス1)を削除すると、後ろの人が自動で前に詰まります。
        // ---------------------------------------------------------
        System.out.println("\n--- 戦士が離脱しました ---");
        party.remove(1); // 1番目の「戦士」を削除
        
        System.out.println("現在のパーティ: " + party);
        // -> [勇者, 魔法使い]
        // 魔法使いが自動的に index 1 に移動しています。
        System.out.println("現在のインデックス1: " + party.get(1));

        // ---------------------------------------------------------
        // 5. 途中への挿入
        // 勇者と魔法使いの間に「僧侶」を割り込ませる
        // ---------------------------------------------------------
        party.add(1, "僧侶"); // index 1 に挿入
        System.out.println("僧侶加入後: " + party);
    }
}