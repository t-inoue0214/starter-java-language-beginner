package com.example.basics_control;

/**
 * 反復（Loop）の基本を学ぶクラスです。
 * 3種類のループ処理を比較します。
 */
public class LoopVariations {

    public static void main(String[] args) {
        
        // ---------------------------------------------------------
        // 1. 基本の for文
        // 「何回繰り返すか」を数字で管理したいときに使います。
        // ---------------------------------------------------------
        System.out.println("=== 基本の for文 ===");
        
        // カウンタ変数 i を0からスタート; iが3より小さい間続ける; 1周ごとにiを1増やす
        for (int i = 0; i < 3; i++) {
            // 現在の i の値を表示（0, 1, 2 と変化する）
            System.out.println("カウント: " + i);
        }

        // ---------------------------------------------------------
        // 2. 拡張 for文 (Enhanced for-loop)
        // データが並んでいるもの（配列など）を、「最初から最後まで」処理するときに使います。
        // ※ int[] は「整数の箱が並んだセット」です（詳しくは第3章でやります）
        // ---------------------------------------------------------
        int[] scores = {10, 20, 30}; // 3つの数字が入ったセットを用意

        System.out.println("\n=== 拡張 for文 ===");
        
        // scoresの中身を、ひとつずつ取り出して score という箱に入れながら繰り返す
        for (int score : scores) {
            // 取り出した中身を表示
            System.out.println("得点: " + score);
        }

        // ---------------------------------------------------------
        // 3. while文
        // 「回数」ではなく「条件」で繰り返したいときに使います。
        // うっかりすると無限ループ（終わらない処理）になりやすいので注意！
        // ---------------------------------------------------------
        System.out.println("\n=== while文 ===");
        
        int hp = 10; // 体力(HP)が10あるとする
        
        // HPが 0 より大きい間、ずっと繰り返す
        while (hp > 0) {
            System.out.println("勇者は戦っている！ (現在HP: " + hp + ")");
            hp = hp - 5; // ダメージを受けてHPを5減らす
        }
        
        // ループを抜けたらここに来る
        System.out.println("勇者は倒れた... (HP: " + hp + ")");
    }
}